#pragma once

#include "table/_TableBase.h"
#include "table/UsageTable.h"

// User-friendly representation of a record in table USAGE_V1.
struct UsageData
{
    int64 m_id; // primary key
    wxString m_date;
    wxString m_json;

    explicit UsageData();
    explicit UsageData(wxSQLite3ResultSet& q);
    UsageData(const UsageData& other) = default;

    int64 id() const { return m_id; }
    void id(const int64 id) { m_id = id; }
    UsageRow to_row() const;
    UsageData& from_row(const UsageRow& row);
    void to_insert_stmt(wxSQLite3Statement& stmt, int64 id) const;
    void to_update_stmt(wxSQLite3Statement& stmt) const;
    UsageData& from_select_result(wxSQLite3ResultSet& q);
    wxString to_json() const;
    void as_json(PrettyWriter<StringBuffer>& json_writer) const;
    row_t to_html_row() const;
    void to_html_template(html_template& t) const;
    void destroy() { delete this; }

    UsageData& clone_from(const UsageData& other);
    bool equals(const UsageData* other) const;
    bool operator< (const UsageData& other) const { return id() < other.id(); }
    bool operator< (const UsageData* other) const { return id() < other->id(); }

    struct SorterByUSAGEID
    {
        bool operator()(const UsageData& x, const UsageData& y)
        {
            return x.m_id < y.m_id;
        }
    };

    struct SorterByUSAGEDATE
    {
        bool operator()(const UsageData& x, const UsageData& y)
        {
            return x.m_date < y.m_date;
        }
    };

    struct SorterByJSONCONTENT
    {
        bool operator()(const UsageData& x, const UsageData& y)
        {
            return x.m_json < y.m_json;
        }
    };
};

inline UsageData::UsageData(wxSQLite3ResultSet& q)
{
    from_select_result(q);
}

inline void UsageData::to_insert_stmt(wxSQLite3Statement& stmt, int64 id) const
{
    to_row().to_insert_stmt(stmt, id);
}

inline void UsageData::to_update_stmt(wxSQLite3Statement& stmt) const
{
    to_row().to_update_stmt(stmt);
}

inline UsageData& UsageData::from_select_result(wxSQLite3ResultSet& q)
{
    return from_row(UsageRow().from_select_result(q));
}

inline wxString UsageData::to_json() const
{
    return to_row().to_json();
}

inline void UsageData::as_json(PrettyWriter<StringBuffer>& json_writer) const
{
    to_row().as_json(json_writer);
}

inline row_t UsageData::to_html_row() const
{
    return to_row().to_html_row();
}

inline void UsageData::to_html_template(html_template& t) const
{
    to_row().to_html_template(t);
}

inline UsageData& UsageData::clone_from(const UsageData& other)
{
    *this = other;
    id(-1);
    return *this;
}
